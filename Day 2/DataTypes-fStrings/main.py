# Data Types
# Strings
print("Hello, world!")
print("Hello"[2])  # Subscript
# Integers
print(123+456)
print(123_456_789)
# Floats
print("Value of PI:")
print(3.14159)
# Boolean
print(True)
print(False)
# To check type of any variable or variable
print(type(123_456.78))

# Type Conversion
print(bool(123))
print("Value of PI: " + str(3.14159))

# f-strings
first_name = "Shivansh"
last_name = "Gupta"
print(f"My name is {first_name} {last_name}")

print(6+4/2-(1*2))

